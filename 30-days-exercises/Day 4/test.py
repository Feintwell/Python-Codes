# 8 + 6 = 14
# 8 - 6 = 2
# 8 * 6 = 48
# 8 / 6 = 1.33
# 8 % 6 = 2
# 8 // 6 = 1
# 8 ** 6 = 262144

q = 8
w = 6

print("{} + {} = {}".format(q, w, q + w))
print("{} - {} = {}".format(q, w, q - w))
print("{} * {} = {}".format(q, w, q * w))
print("{} / {} = {:.2f}".format(q, w, q / w))
print("{} % {} = {}".format(q, w, q % w))
print("{} // {} = {}".format(q, w, q // w))
print("{} ** {} = {}".format(q, w, q ** w))